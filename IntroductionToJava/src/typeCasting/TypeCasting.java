package typeCasting;

public class TypeCasting {

	public static void main(String[] args) {
		
//		atomatic typecasting
		int a=3;
		double d =6.333;
		
		double f=a;
		System.out.println(f);
		
		//manual typecasting
		
		double mydouble =3.525552;
		int myint = (int) mydouble;
		
		System.out.println(mydouble);
			
		
		
		
	}

}
